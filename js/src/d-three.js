$(function(){

    var chart = d3.select('.chart');
    var bars = chart.selectAll('div');
    var data = [12, 30, 9, 47, 80, 64, 100];

    bars.data(data)
            .enter().append('div')
                .classed('bar', true)
                .style('width', function(d){ return d + '%'; })
                .text(function(d){ return d });
}); // end doc ready